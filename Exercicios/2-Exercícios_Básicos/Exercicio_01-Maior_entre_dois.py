
# DESCUBRA QUAL O MAIOR NÚMERO ENTRE OS DOIS DIGITADOS PELO USUÁRIO

print("DESCUBRA QUAL O MAIOR NÚMERO !! \n")

NUM_1 = int(input("Digite o 1° numero: "))

NUM_2 = int(input("Digite o 2° numero: "))


if (NUM_1 > NUM_2):
    print("\nO primeiro numero é maior !!")

if (NUM_1 < NUM_2):
    print("\nO segundo numero é maior !!")


if (NUM_1 == NUM_2):
    print("\nO número é o mesmo !!")
